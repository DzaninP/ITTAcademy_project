
package bean;


public class Card {
    private int id;
    private String cardName, cardInfo, cardOpen, cardManagment;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCardName() {
        return cardName;
    }

    public void setCardName(String cardName) {
        this.cardName = cardName;
    }

    public String getCardInfo() {
        return cardInfo;
    }

    public void setCardInfo(String cardInfo) {
        this.cardInfo = cardInfo;
    }

    public String getCardOpen() {
        return cardOpen;
    }

    public void setCardOpen(String cardOpen) {
        this.cardOpen = cardOpen;
    }

    public String getCardManagment() {
        return cardManagment;
    }

    public void setCardManagment(String cardManagment) {
        this.cardManagment = cardManagment;
    }
    
    
    
}
