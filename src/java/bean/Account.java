
package bean;

public class Account {
    private int id;
    private String accountName,accountInfo,accountOpen,accountManagment;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAccountName() {
        return accountName;
    }

    public void setAccountName(String accountName) {
        this.accountName = accountName;
    }

    public String getAccountInfo() {
        return accountInfo;
    }

    public void setAccountInfo(String accountInfo) {
        this.accountInfo = accountInfo;
    }

    public String getAccountOpen() {
        return accountOpen;
    }

    public void setAccountOpen(String accountOpen) {
        this.accountOpen = accountOpen;
    }

    public String getAccountManagment() {
        return accountManagment;
    }

    public void setAccountManagment(String accountManagment) {
        this.accountManagment = accountManagment;
    }
    
    
    
}
